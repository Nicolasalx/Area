export const getServiceColor = (
  service: string,
  intensity: 50 | 500 = 500,
): string => {
  switch (service) {
    case "google":
      return `blue-${intensity}`;
    case "github":
      return `gray-${intensity === 50 ? 100 : 600}`;
    case "time":
      return `gray-${intensity === 50 ? 100 : 600}`;
    case "discord":
      return `indigo-${intensity}`;
    case "slack":
      return `emerald-${intensity}`;
    case "trello":
      return `cyan-${intensity}`;
    case "rss":
      return `orange-${intensity}`;
    case "todoist":
      return `red-${intensity}`;
    default:
      return intensity === 50 ? "white" : `gray-${intensity}`;
  }
};

export const getServiceTextColor = (service: string): string => {
  return `text-${getServiceColor(service)}`;
};

export const getServiceGradient = (
  service: string,
  position: "from" | "to",
): string => {
  return `${position}-${getServiceColor(service, 50)}`;
};

